package com.huawei.bigdata.oozie.examples;

public class Person
{
    String name;

    int account;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getAccount()
    {
        return account;
    }

    public void setAccount(int account)
    {
        this.account = account;
    }
}
